import java.util.*;
public class sqrt {
    public static void sqrtt(int x){
        System.out.print(Math.sqrt(x));
    }
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter a number :");
        int a = sc.nextInt();
        sqrtt(a);
    }
}
