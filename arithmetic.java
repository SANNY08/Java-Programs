import java.util.*;
public class arithmetic {
    public static void main(String args[]){
        int a = 10;
        int b =5;
        int Sum = (a+b);
        int Sub = (a-b);
        int Mul = (a*b);
        int Div = (a/b);
        int Modula = (a%b);
        System.out.println("Add :"+Sum);
        System.out.println("Sub :"+Sub);
        System.out.println("Mul :"+Mul);
        System.out.println("Div :"+Div);
        System.out.println("Modula :"+Modula);
    }
    
}
